    private void ActivityHrKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ActivityHrKeyReleased
        try{
        int timeHrVal=Integer.parseInt(ActivityHr.getText());
        if(timeHrVal > 12){
            if(timeHrVal == 13){
                ActivityHr.setText(""+1);
            }else if(timeHrVal == 14){
                ActivityHr.setText(""+2);
            }else if(timeHrVal == 15){
                ActivityHr.setText(""+3);;
            }else if(timeHrVal == 16){
                ActivityHr.setText(""+4);
            }else if(timeHrVal == 17){
                ActivityHr.setText(""+5);
            }else if(timeHrVal == 18){
                ActivityHr.setText(""+6);
            }else if(timeHrVal == 19){
                ActivityHr.setText(""+7);
            }else if(timeHrVal == 20){
                ActivityHr.setText(""+8);
            }else if(timeHrVal == 21){
                ActivityHr.setText(""+9);
            }else if(timeHrVal == 22){
                ActivityHr.setText(""+10);
            }else if(timeHrVal == 23){
                ActivityHr.setText(""+11);
            }else if(timeHrVal >= 24){
                ActivityHr.setText(""+12);
            }
        
            AM_PM();
        }else if( timeHrVal < 1 ){ActivityHr.setText(""+1);}  
        
        }catch(Exception ex){}

        CheckField();
    }//GEN-LAST:event_ActivityHrKeyReleased

