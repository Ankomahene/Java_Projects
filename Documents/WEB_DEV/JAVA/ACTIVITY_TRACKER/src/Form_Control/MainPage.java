    private void SaveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SaveBtnActionPerformed
       if (IdLbl.getText().isEmpty()){
            if( ((JTextComponent)ActivityDate.getDateEditor().getUiComponent()).getText().isEmpty()){
           JOptionPane.showMessageDialog(null,"Please Date is Required");
       }
        else{ 
               
            if(ActivityVenue.getText().isEmpty() && !NoVenue.isSelected()){
                JOptionPane.showMessageDialog(null,"Set Venue or Check 'Does not require venue' to Ignore");
            }else{
           if((ActivityHr.getText().isEmpty() ||  ActivityMin.getText().isEmpty()) && !NoTime.isSelected()){
                JOptionPane.showMessageDialog(null,"Set Time or Check 'Does not require time' to Ignore");
              }
            else{
               InsertData();
           }
           
            }    
        }
            
       }//End of if Label is Empty
       else{
           updateData();
       }
        
     
    }//GEN-LAST:event_SaveBtnActionPerformed

