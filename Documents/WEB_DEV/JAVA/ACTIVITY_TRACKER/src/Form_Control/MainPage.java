    private void editActActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActActionPerformed
        
        
        if(Starred_Activities.getSelectionModel().isSelectionEmpty()){
            JOptionPane.showMessageDialog(null, "No activity is selected");
        }
        else{
            int row=Starred_Activities.getSelectedRow();
            String id = (Starred_Activities.getModel().getValueAt(row, 0).toString());
            String A_name = (Starred_Activities.getModel().getValueAt(row, 1).toString());
            String A_date = (Starred_Activities.getModel().getValueAt(row, 2).toString());
            String A_venue = (Starred_Activities.getModel().getValueAt(row, 3).toString());
            
        IdLbl.setText(id);
        ActivityName.setText(A_name);
        ((JTextComponent)ActivityDate.getDateEditor().getUiComponent()).setText(A_date);
        ActivityVenue.setText(A_venue);
       
        GotoAddNewActivityPanel();
       
        }
        
        hideTablesColumns();
    }//GEN-LAST:event_editActActionPerformed

